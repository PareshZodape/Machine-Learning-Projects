import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# load the dataset
message = pd.read_csv('/content/SMSSpamCollection.csv', sep ='\t', names = ['label', 'message'])

message

message.shape

message.isnull().sum()

"""# Data Cleaning and Preprocessing"""

import re # Regular Expression for removing special charecters
import nltk
nltk.download('stopwords')

from nltk.corpus import stopwords
from nltk.stem.porter import PorterStemmer
ps = PorterStemmer()

# Apply Stemming
corpus = []
for i in range(0, len(message)):
  review = re.sub('[^a-zA-Z0-9]', " ", message['message'][i]) #remove all the special charecter other than a-z and A-Z
  review = review.lower()
  review = review.split()

  review = [ps.stem(word) for word in review if not word in stopwords.words('english')]
  review = " ".join(review)
  corpus.append(review)

corpus

"""# Create Vecotrs using TF-IDF Vectorizer"""

from sklearn.feature_extraction.text import TfidfVectorizer
cv = TfidfVectorizer(max_features = 2500, ngram_range = (2,3))
X = cv.fit_transform(corpus).toarray()

y = pd.get_dummies(message['label'])
y = y.iloc[:, 1].values

print(X)

print(y)

"""# Train Test Split"""

from sklearn.model_selection import train_test_split
X_train, X_test , y_train, y_test = train_test_split(X, y, test_size =0.2, random_state = 0)

from sklearn.naive_bayes import MultinomialNB
model = MultinomialNB().fit(X_train, y_train)

# prediction
y_pred = model.predict(X_test)

"""# Model Evaluation"""

from sklearn.metrics import accuracy_score, classification_report

print("The accuracy of model is: ", accuracy_score(y_pred, y_test))

print(classification_report(y_pred, y_test))

# Use another model
from sklearn.ensemble import RandomForestClassifier
classifier = RandomForestClassifier()
classifier.fit(X_train, y_train)

# prediction
y_pred = classifier.predict(X_test)

# model evaluation
print(accuracy_score(y_pred, y_test))
print(classification_report(y_pred, y_test))

