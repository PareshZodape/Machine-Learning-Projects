mean --  average_value

mode -- frequent_value
"""

# impute numerical column by mean
df['Item_Weight'].mean()

df['Item_Weight'].fillna(df['Item_Weight'].mean(), inplace = True)

"""### Replacing the missing values in "Outlet_Size' with mode"""

mode_of_outlet_size = df.pivot_table(values='Outlet_Size', columns = 'Outlet_Type', aggfunc=(lambda x : x.mode()[0] ))

# 'Outlet_Size' & 'Outlet_Type' are strongly correlated with each Other thats why we choose these two columns

# pivot_table(): This is a powerful Pandas function used for creating a spreadsheet-style pivot table as a DataFrame. It takes various arguments to define how data should be aggregated and displayed.

# values='Outlet_Size': This argument specifies the column whose values will be aggregated. In this case, we are interested in the Outlet_Size column.

# columns='Outlet_Type': This argument defines the column(s) that will form the columns of the pivot table. Here, the unique values from the Outlet_Type column will become the new column headers.

# aggfunc=(lambda x : x.mode()[0]): This is the aggregation function applied to the values.

# lambda x : defines an anonymous function.
# x.mode() finds the most frequently occurring value(s) in a series (in this context, for Outlet_Size within each Outlet_Type group).
# [0] selects the first mode if there are multiple modes (i.e., if there's a tie for the most frequent value). So, for each Outlet_Type, this will return the most common Outlet_Size.

print(mode_of_outlet_size)

# After taking mode of all the category we got this observation
# Observation:
   # Outlet_Type = 'Grocery Store',      then Outlet_Size = Small
   # Outlet_Type = 'Supermarket Type 1', then Outlet_Size = Small
   # Outlet_Type = 'Supermarket Type 2', then Outlet_Size = medium
   # Outlet_Type = 'Supermarket Type 1', then Outlet_Size = medium

missing_values = df['Outlet_Size'].isnull()

print(missing_values)

df.loc[missing_values, 'Outlet_Size'] = df.loc[missing_values, 'Outlet_Type'].apply(lambda x: mode_of_outlet_size.loc['Outlet_Size', x])

# Observation

# The  cell's execution is that all the missing values in the Outlet_Size column have been successfully filled.
# The code used the most frequent Outlet_Size for each Outlet_Type to intelligently impute these missing entries, ensuring that your dataset is now complete with respect to Item_Weight and Outlet_Size.

